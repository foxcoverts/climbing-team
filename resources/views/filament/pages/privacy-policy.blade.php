        <section>
            <h3>How we get the personal information and why we have it</h3>
            <p>Most of the personal information we process is provided to us directly by you when you join the team.</p>
            <p>We also receive personal information from Scouts UK so that we can manage your training and qualification renewals.</p>
            <p>We use the information that you have given us to manage the climbing team and organise activity bookings.</p>
            <p>We may share this information with Scouts UK and Hinckley District Scouts.</p>
            <p>Under the UK General Data Protection Regulation (<abbr>UK GDPR</abbr>), the lawful bases we rely on for processing this information are:</p>
            <ol type="a">
                <li>Your consent. You can remove your consent at any time by contacting
                    <x-filament::link href="mailto:climbing@foxcoverts.org.uk" size="text-md">climbing@foxcoverts.org.uk</x-filament::link>
                </li>
                <li>We have a legitimate interest.</li>
            </ol>
        </section>

        <section>
            <h3>How we store your
                personal information
            </h3>
            <p>Your information is securely stored.</p>
            <p>We keep your personal information if you are a member of the team, or you are the parent/guardian of a
                young
